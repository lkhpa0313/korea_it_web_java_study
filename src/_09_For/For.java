package _09_For;

public class For {
    public static void main(String[] args) {
        //반복문 - for
        //for ( 선언; 조건; 증감) {
        //      반복될 코드
        // }
        for(int i = 0; i < 10; i++) {
            System.out.println("안녕" + i);
        }

        for (int i = 0; i < 50; i++) {
            System.out.println("ㅎㅇ");
        }
    }
}
